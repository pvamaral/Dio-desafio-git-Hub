# Operadores de atribuição
V_Saldo = 1000
print(f"Saldo = {V_Saldo}")
V_Saldo += 5000
print(f"Saldo += {V_Saldo}")
V_Saldo -= 1000
print(f"Saldo -= {V_Saldo}")
V_Saldo  /= 3
print(f"Saldo /= {V_Saldo}")
V_Saldo //= 2
print(f"Saldo //= {V_Saldo}")
V_Saldo **= 2
print(f"Saldo **=2 {V_Saldo}")
V_Saldo %= 2
print(f"Saldo %=2 {V_Saldo}")
